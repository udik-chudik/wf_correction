function [] = result_show(errors, Z, gain, n_aver)
%UNTITLED Summary of this function goes here
%   Detailed explanation goes here
er_min = min(errors);
er_mean = mean(errors);
less_mean = find(errors < er_mean );

plot(errors);

Y = [2.5, 2.1, 1.9, 1.6, 1.4, 1.2, 1, 0.8, 0.6] - 0.3;

Text = ['Z = ' + string(Z),...
    'min(errors) = ' + string(er_min),...
    'N(min) = ' + string(find(errors == er_min )),...
    'mean(errors(100:200)) = ' + string(er_mean),...
    'first N < N(mean) = ' + string( less_mean(1) ),...
    'var(errors(100:200)) = ' + string(var(errors)),...
    'max(1/errors) = ' + string(1/er_min),...
    'gain = ' + gain,...
    'N aver = ' + n_aver];

for i = 1:length(Y)
    text(50, Y(i), Text(i) );

end